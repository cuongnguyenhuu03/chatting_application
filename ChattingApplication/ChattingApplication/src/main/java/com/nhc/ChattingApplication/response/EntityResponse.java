package com.nhc.ChattingApplication.response;

public class EntityResponse {

    private String EM;
    private int EC;
    private Object DT;

    public String getEM() {
        return EM;
    }

    public void setEM(String eM) {
        EM = eM;
    }

    public int getEC() {
        return EC;
    }

    public void setEC(int eC) {
        EC = eC;
    }

    public Object getDT() {
        return DT;
    }

    public void setDT(Object dT) {
        DT = dT;
    }

}
